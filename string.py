my_variable = "Python"
 
print(my_variable[0]) # 'p'

print(my_variable[2]) # 't'

print(my_variable[-1]) # 'n'

print(my_variable[-2]) # 'o'

print(my_variable[1:4])  # 'yth'
 
print(my_variable[3:])  # 'hon'
 
print(my_variable[:4])  # 'Pyth'
 
print(my_variable[::-1])  # 'nohtyP'
 
print(my_variable[::-2])  # 'nhy'
 
print(my_variable[1:4:2])  # 'yh'

print(len(my_variable))

print(my_variable.count('py'))

print(my_variable.lower())

print(my_variable.upper())

